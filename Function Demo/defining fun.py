def sum_with_parameters(x, y):
    print("The sum of", x, "and", y, "is:", x + y)

sum_with_parameters(3, 7)


def product_with_parameters(x, y):
    print("The product of", x, "and", y, "is:", x * y)

product_with_parameters(3, 7)












    